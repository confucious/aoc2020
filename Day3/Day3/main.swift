struct Point: Equatable & Hashable {
    let x: Int
    let y: Int

    func offset(right: Int, down: Int) -> Point {
        return Point(x: x + right, y: y + down)
    }
}

enum Thing {
    case empty
    case tree
}

struct Map {
    var map = [Point:Thing]()
    var width: Int
    var height: Int
    init(input: String) {
        let lines = input.split(separator: "\n")
        self.height = lines.count
        self.width = lines[0].count
        for (row, line) in lines.enumerated() {
            for (col, char) in line.enumerated() {
                let thing: Thing
                switch char {
                case ".": thing = .empty
                case "#": thing = .tree
                default:
                    fatalError("Unknown \(char)")
                }
                map[Point(x: col, y: row)] = thing
            }
        }
    }

    func thing(at point: Point) -> Thing {
        let targetX = point.x % width
        return map[Point(x: targetX, y: point.y)]!
    }
}

func test(map: Map, right: Int, down: Int) -> Int {
    var point = Point(x: 0, y: 0)
    var treeCount = 0
    while point.y < map.height {
        if map.thing(at: point) == .tree {
            treeCount += 1
        }
        point = point.offset(right: right, down: down)
    }
    return treeCount
}

func part1() {
    let map = Map(input: input)
    let treeCount = test(map: map, right: 3, down: 1)
    print(treeCount)
}

func part2() {
    let map = Map(input: input)
    let treeCount1 = test(map: map, right: 1, down: 1)
    let treeCount2 = test(map: map, right: 3, down: 1)
    let treeCount3 = test(map: map, right: 5, down: 1)
    let treeCount4 = test(map: map, right: 7, down: 1)
    let treeCount5 = test(map: map, right: 1, down: 2)
    print("\(treeCount1) * \(treeCount2) * \(treeCount3) * \(treeCount4) * \(treeCount5) = \(treeCount1 * treeCount2 * treeCount3 * treeCount4 * treeCount5)")
}

part2()
